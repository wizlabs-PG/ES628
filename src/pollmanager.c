//       _event : 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾏｷ魹ｽ魹ｽ魹ｽ 魹ｽﾌｺ魹ｽﾆｮ 魹ｽ魹ｽ魹ｽ魹ｽ
// 魹ｽ魹ｽﾈｯ: 0                  : 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ
//       POLL_NO_DESCRIPTOR : 魹ｽﾎｼ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽﾅｩ魹ｽ魹ｽ魹ｽﾍｰ魹ｽ poll 魹ｽﾞｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
//                            魹ｽ魹ｽﾏｵﾇｾ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ
//--------------------------------------------------------------------
int poll_change_event( int _fd, int _event)
{
  int   ndx;

  for( ndx = 0; ndx < POLL_MAX_COUNT; ndx++ )                         
  {
    if ( _fd == poll_array[ndx].fd)
    {
      poll_array[ndx].events  = _event;
      return 0;
    }
  }
  return POLL_NO_DESCRIPTOR;
}

//--------------------------------------------------------------------
// 魹ｽ魹ｽ魹ｽ魹ｽ: poll 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ
// 魹ｽ魹ｽﾈｯ: POLL_BIG_ERR   - ﾄ｡魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾟｻ魹ｽ
//       POLL_TIME_OUT  - 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾃｰ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾌｺ魹ｽﾆｮ魹ｽ魹ｽ 魹ｽﾟｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾊｾ魹ｽ魹ｽ魹ｽ
//       POLL_EVENTED   - ﾆｯ魹ｽ魹ｽ 魹ｽﾌｺ魹ｽﾆｮ魹ｽ魹ｽ 魹ｽﾟｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
//--------------------------------------------------------------------
int poll_loop( int _time_out)
{
  int     poll_state;
  int     ndx;

  poll_state = poll( (struct pollfd *)&poll_array, POLL_MAX_COUNT, _time_out);
  if ( 0 > poll_state)
  {                                                                   // ﾄ｡魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾟｻ魹ｽ魹ｽﾟｴ魹ｽ.
    return POLL_BIG_ERR;
  }
  if ( 0 == poll_state)                                               // ﾅｸ魹ｽﾓｾﾆｿ魹ｽ魹ｽ魹ｽ 魹ｽﾉｷ魹ｽ魹ｽ魹ｽ魹ｽﾇｷ魹ｽ
  {                                                                   // 魹ｽﾙｽ魹ｽ 魹ｽ魹ｽ魹ｽﾂｸ魹ｽ ﾈｮ魹ｽ魹ｽ魹ｽﾑｴ魹ｽ.
    return POLL_TIME_OUT;
  }
  for( ndx = 0; ndx < POLL_MAX_COUNT; ndx++ )      
  {
    if( poll_array[ndx].revents & POLLIN )                            // 魹ｽ魹ｽ魹ｽﾎｺ魹ｽ魹ｽ魹ｽ 魹ｽﾚｷ皺｡ 魹ｽ魹ｽ魹ｽﾅｵﾇｾ魹ｽ魹ｽ魹ｽ
    {
      if ( NULL != poll_objs[ndx])
      {
        poll_objs[ndx]->on_poll_in( poll_objs[ndx]);
      }
    }
    if( poll_array[ndx].revents & POLLOUT )                           // 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽﾂｰ魹ｽ block 魹ｽ魹ｽ 魹ｽﾆｴ魹ｽ
    {
      if ( NULL != poll_objs[ndx])
      {
        poll_objs[ndx]->on_poll_out( poll_objs[ndx]);
      }
    }
    if( poll_array[ndx].revents & POLLERR )                           // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾟｻ魹ｽ
    {
      if ( NULL != poll_objs[ndx])
      {
        poll_objs[ndx]->on_poll_err( poll_objs[ndx]);
      }
    }
    if( poll_array[ndx].revents & POLLHUP )                           // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽﾙｸ魹ｽ
    {
      if ( NULL != poll_objs[ndx])
      {
        poll_objs[ndx]->on_poll_hup( poll_objs[ndx]);
      }
    }
  }
  return  POLL_EVENTED;
}
