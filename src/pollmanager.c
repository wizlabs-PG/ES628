// 魹ｽ魹ｽ魹ｽ魹ｽ: poll 魹ｽﾞｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ 魹ｽﾖｴ魹ｽ 魹ｽﾎｵ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽﾈ｣魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽﾑｴ魹ｽ.
// 魹ｽ魹ｽﾈｯ: 魹ｽ霑ｭ 魹ｽ魹ｽ 魹ｽ魹ｽﾏｵ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾎｵ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽﾈ｣
//--------------------------------------------------------------------
int poll_blank_ndx( void)
{
  int poll_ndx;
  int ndx;

                                  // poll array 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽﾅｩ魹ｽ魹ｽ魹ｽﾍｰ魹ｽ -1 魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
                                  // poll 魹ｽﾋｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾊｴ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾌｴ魹ｽ.
                                  // 魹ｽ魹ｽ魹ｽ 魹ｽﾖｴ魹ｽ 魹ｽ霑ｭ魹ｽ魹ｽ 魹ｽﾎｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾋｻ魹ｽ魹ｽﾑｴ魹ｽ.

  poll_ndx  = -1;
  for ( ndx = 0; ndx < POLL_MAX_COUNT; ndx++)
  {
    if ( -1 == poll_array[ndx].fd)
    {
      poll_ndx  = ndx;                                                // 魹ｽ魹ｽ魹ｽ 魹ｽﾖｴ魹ｽ  array 魹ｽ魹ｽ ﾃ｣魹ｽ魹ｽ
      break;
    }
  }
  return poll_ndx;
}

//--------------------------------------------------------------------
// 魹ｽ魹ｽ魹ｽ魹ｽ: poll 魹ｽﾞｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ net_obj 魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ
//--------------------------------------------------------------------
int poll_register( net_obj_t *_obj)
{
  int poll_ndx;

                                  // 魹ｽ魹ｽ魹ｽ 魹ｽﾖｴ魹ｽ poll array 魹ｽ魹ｽ ﾃ｣魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽﾑｴ魹ｽ.
                                  // poll array 魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ 魹ｽﾖｴ魹ｽ 魹ｽﾎｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ
                                  // poll_ndx 魹ｽ魹ｽ 0 魹ｽﾌｻ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽﾌｵﾈｴ魹ｽ.

  poll_ndx  = poll_blank_ndx();

  if ( 0 <= poll_ndx)                                                 // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ 魹ｽﾖｴ魹ｽ poll array 魹ｽ魹ｽ ﾃ｣魹ｽ魹ｽ魹ｽ魹ｽ
  {
    _obj->poll_ndx                = poll_ndx;                         // socket 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ
    poll_array[poll_ndx].fd       = _obj->fd;                         // poll 魹ｽ魹ｽ 魹ｽ魹ｽﾅｩ魹ｽ魹ｽ魹ｽﾍｸ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽﾑｴ魹ｽ.
    poll_array[poll_ndx].events   = POLLIN | POLLERR | POLLHUP;       // POLLOUT 魹ｽ魹ｽ poll_change_event() 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ
    poll_array[poll_ndx].revents  = 0;
    poll_objs[poll_ndx]           = _obj;
  }
  return poll_ndx;
}

